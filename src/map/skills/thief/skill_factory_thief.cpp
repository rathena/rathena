		case SHC_SAVAGE_IMPACT:
			return std::make_unique<SkillSavageImpact>();
		case SHC_SHADOW_EXCEED:
			return std::make_unique<StatusSkillImpl>(skill_id);
		case SHC_SHADOW_STAB:
			return std::make_unique<SkillShadowStab>();
