		case TF_HIDING:
			return std::make_unique<SkillHiding>();
		case TF_PICKSTONE:
			return std::make_unique<SkillFindStone>();
		case TF_POISON:
			return std::make_unique<SkillEnvenom>();
		case TF_SPRINKLESAND:
			return std::make_unique<SkillSandAttack>();
		case TF_STEAL:
