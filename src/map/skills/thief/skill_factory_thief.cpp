		case ABC_ABYSS_SLAYER:
			return std::make_unique<StatusSkillImpl>(skill_id);
		case ABC_CHAIN_REACTION_SHOT_ATK:
			return std::make_unique<WeaponSkillImpl>(skill_id);
		case ASC_BREAKER:
			return std::make_unique<SkillSoulDestroyer>();
		case ASC_CDP:
